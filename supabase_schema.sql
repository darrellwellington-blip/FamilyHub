  on conflict (meal_try_id, user_id) do update set vote = p_vote, voted_at = now();
end;
$$;
grant execute on function upsert_meal_try_vote to anon, authenticated;

-- ─── RPC: record shopping purchase ───────────────────────────────────────────

create or replace function record_shopping_purchase(
  p_list_id       bigint,
  p_purchased_ids bigint[],
  p_carry_back_ids bigint[],
  p_notes         text default null
)
returns bigint language plpgsql security definer as $$
declare
  v_store      text;
  v_purchase_id bigint;
  v_item       record;
begin
  select store_name into v_store from shopping_lists where id = p_list_id;

  insert into purchase_records (list_id, store_name, notes)
  values (p_list_id, v_store, p_notes)
  returning id into v_purchase_id;

  -- Save purchased items to record
  for v_item in
    select * from shopping_items
    where id = any(p_purchased_ids) and list_id = p_list_id
  loop
    insert into purchase_record_items (purchase_id, name, quantity, unit, is_recurring)
    values (v_purchase_id, v_item.name, v_item.quantity, v_item.unit, v_item.is_recurring);

    if v_item.id = any(p_carry_back_ids) then
      -- Uncheck and keep on list
      update shopping_items set is_checked = false, checked_by = null, checked_at = null
      where id = v_item.id;
    else
      -- Remove from list
      delete from shopping_items where id = v_item.id;
    end if;
  end loop;

  return v_purchase_id;
end;
$$;
grant execute on function record_shopping_purchase to anon, authenticated;

-- ─── RPC: new shopping trip from template ────────────────────────────────────

create or replace function new_shopping_trip(p_template_id bigint)
returns bigint language plpgsql security definer as $$
declare
  v_tmpl  record;
  v_name  text;
  v_trip_id bigint;
  v_item  record;
begin
  select * into v_tmpl from shopping_lists where id = p_template_id and is_template = true;
  if not found then raise exception 'Template not found'; end if;

  v_name := v_tmpl.name || ' – ' || to_char(now(), 'Mon DD');
  insert into shopping_lists (name, store_name, created_by, is_template, template_id)
  values (v_name, v_tmpl.store_name, v_tmpl.created_by, false, p_template_id)
  returning id into v_trip_id;

  for v_item in select * from shopping_items where list_id = p_template_id loop
    insert into shopping_items (list_id, name, quantity, unit, description, notes, is_recurring, added_by)
    values (v_trip_id, v_item.name, v_item.quantity, v_item.unit, v_item.description, v_item.notes, v_item.is_recurring, v_item.added_by);
  end loop;

  return v_trip_id;
end;
$$;
grant execute on function new_shopping_trip to anon, authenticated;

-- ─── RPC: upsert meal plan slot ──────────────────────────────────────────────

create or replace function upsert_meal_plan_slot(
  p_week_start     text,
  p_day_of_week    integer,
  p_meal_type      text,
  p_slot_type      text,
  p_meal_id        bigint default null,
  p_restaurant_id  bigint default null,
  p_leftovers_note text   default null,
  p_cook_id        bigint default null,
  p_attendee_ids   bigint[] default null
)
returns bigint language plpgsql security definer as $$
declare
  v_slot_id bigint;
begin
  insert into meal_plan_slots
    (week_start, day_of_week, meal_type, slot_type, meal_id, restaurant_id, leftovers_note, cook_id)
  values
    (p_week_start, p_day_of_week, p_meal_type, p_slot_type, p_meal_id, p_restaurant_id, p_leftovers_note, p_cook_id)
  on conflict (week_start, day_of_week, meal_type) do update set
    slot_type      = excluded.slot_type,
    meal_id        = excluded.meal_id,
    restaurant_id  = excluded.restaurant_id,
    leftovers_note = excluded.leftovers_note,
    cook_id        = excluded.cook_id
  returning id into v_slot_id;

  -- Replace attendees
  delete from meal_plan_slot_attendees where slot_id = v_slot_id;
  if p_attendee_ids is not null then
    insert into meal_plan_slot_attendees (slot_id, user_id)
    select v_slot_id, unnest(p_attendee_ids)
    on conflict do nothing;
  end if;

  return v_slot_id;
end;
$$;
grant execute on function upsert_meal_plan_slot to anon, authenticated;
