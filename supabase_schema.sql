-- ─── RPC: filter escape rooms ─────────────────────────────────────────────────

create or replace function filter_escape_rooms(
  p_city_id    bigint,
  p_user_ids   bigint[],
  p_friend_ids bigint[],
  p_mode       text   -- 'new' or 'completed'
)
returns jsonb
language plpgsql
security definer
as $$
declare
  v_result jsonb;
begin
  select jsonb_agg(venue_row order by venue_row->>'name')
  into v_result
  from (
    select jsonb_build_object(
      'id',    v.id,
      'name',  v.name,
      'rooms', (
        select jsonb_agg(room_row order by room_row->>'name')
        from (
          select jsonb_build_object(
            'id',               r.id,
            'name',             r.name,
            'difficulty',       r.difficulty,
            'min_participants', r.min_participants,
            'max_participants', r.max_participants,
            'total_completions', (
              select count(*) from escape_room_completions c where c.room_id = r.id
            ),
            'last_played_at', (
              select max(c.played_at) from escape_room_completions c where c.room_id = r.id
            ),
            'last_escaped', (
              select c.escaped from escape_room_completions c
              where c.room_id = r.id order by c.played_at desc nulls last limit 1
            )
          ) as room_row
          from escape_rooms r
          where r.venue_id = v.id
            and r.is_active = true
            and (
              case when p_mode = 'completed' then (
                -- at least one completion where ALL selected people participated
                exists (
                  select 1 from escape_room_completions c
                  where c.room_id = r.id
                    and (
                      array_length(p_user_ids, 1) is null or
                      not exists (
                        select unnest(p_user_ids) except
                        select ercu.user_id from escape_room_completion_users ercu where ercu.completion_id = c.id
                      )
                    )
                    and (
                      array_length(p_friend_ids, 1) is null or
                      not exists (
                        select unnest(p_friend_ids) except
                        select ercf.friend_id from escape_room_completion_friends ercf where ercf.completion_id = c.id
                      )
                    )
                )
              ) else (
                -- no completion where any selected person participated
                not exists (
                  select 1 from escape_room_completions c
                  where c.room_id = r.id
                    and (
                      (array_length(p_user_ids, 1) is not null and
                       exists (select 1 from escape_room_completion_users ercu where ercu.completion_id = c.id and ercu.user_id = any(p_user_ids)))
                      or
                      (array_length(p_friend_ids, 1) is not null and
                       exists (select 1 from escape_room_completion_friends ercf where ercf.completion_id = c.id and ercf.friend_id = any(p_friend_ids)))
                    )
                )
              ) end
            )
        ) sub
      )
    ) as venue_row
    from escape_room_venues v
    where v.city_id = p_city_id
  ) venues;

  return coalesce(v_result, '[]'::jsonb);
end;
$$;

grant execute on function filter_escape_rooms to anon, authenticated;

-- ─── RPC: upsert meal rating ──────────────────────────────────────────────────

create or replace function upsert_meal_rating(
  p_meal_id bigint,
  p_user_id bigint,
  p_rating  integer
)
returns void language plpgsql security definer as $$
begin
  insert into meal_ratings (meal_id, user_id, rating)
  values (p_meal_id, p_user_id, p_rating)
  on conflict (meal_id, user_id) do update set rating = p_rating, rated_at = now();
end;
$$;
grant execute on function upsert_meal_rating to anon, authenticated;

